
def add_numbers(num1, num2):
    """
    Returns the sum of num1 and num2.
    """
    return num1 + num2

def is_even(number):
    """
    Returns True if the number is even, otherwise False.
    """
    return number % 2 == 0

def reverse_string(text):
    """
    Returns the reversed version of the input string.
    """
    return text[::-1]